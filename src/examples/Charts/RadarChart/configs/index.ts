import { ChartDataset } from 'chart.js';
import { ColorType } from 'types';

function configs(labels: string[], datasets: (ChartDataset<'radar'> & { color: ColorType })[]) {
  return {
    data: {
      labels,
      datasets: [...datasets],
    },
    options: {
      responsive: true,
      maintainAspectRatio: false,
      plugins: {
        legend: {
          display: false,
        },
      },
      interaction: {
        intersect: false,
        mode: 'index' as const,
      },
    },
  };
}

export default configs;
